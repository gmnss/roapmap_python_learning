"""
=   Asigna valores del lado derecho a la variable del lado izquierdo.
+=  Agrega el valor derecho a la variable izquierda y asigna el resultado a la variable izquierda.
-=  Sustrae el valor derecho a la variable izquierda y asigna el resultado a la variable izquierda.
/=  Divide el valor derecho a la variable izquierda y asigna el resultado a la variable izquierda.
//= Realiza la división de piso entre los 2 valores (número entero) y asigna valor a la variable izquierda.
%=  Entrega el módulo usando los 2 valores y asigna el resultado a la variable izquierda.
*=  Multiplica el valor derecho a la variable izquierda y asigna el resultado a la variable izquierda.
**= Realiza cálculos exponenciales (potencia) entre los valores y asigna valor a la variable izquierda.
"""

# =   Asigna valores del lado derecho a la variable del lado izquierdo.
variable1 = 100
print(variable1)

# +=  Agrega el valor derecho a la variable izquierda y asigna el resultado a la variable izquierda.
variable1 += 1
print(variable1)

# -=  Sustrae el valor derecho a la variable izquierda y asigna el resultado a la variable izquierda.
variable1 -= 1
print(variable1)

# /=  Divide el valor derecho a la variable izquierda y asigna el resultado a la variable izquierda.
variable1 /= 2
print(variable1)

# //= Realiza la división de piso entre los 2 valores (número entero) y asigna valor a la variable izquierda.
variable1 //= 2.3
print(variable1)

# %=  Entrega el módulo usando los 2 valores y asigna el resultado a la variable izquierda.
variable1 %= 6
print(variable1)

# *=  Entrega el módulo usando los 2 valores y asigna el resultado a la variable izquierda.
variable1 *= 2
print(variable1)

# **= Realiza cálculos exponenciales (potencia) entre los valores y asigna valor a la variable izquierda.
variable1 **= 5
print(variable1)